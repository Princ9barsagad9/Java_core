public class Character {
        public static void main(String[] args) {
            System.out.println("The Capital characters are:");
            for (int i=65;i<=90;i++){
                System.out.print((char)i+" ");
            }
            System.out.println("");
            System.out.println("The small characters are:");
            for (int i=97;i<=122;i++){
                System.out.print((char)i+" ");
            }
        }
    }

