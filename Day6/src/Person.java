public class Person {
    private String name;
    private int id;

    public Person() {
        this.name = name;
        System.out.println("person");
    }

    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
}
